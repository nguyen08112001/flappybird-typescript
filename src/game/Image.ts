class SpriteImage {
    public sX: number
    public sY: number
    public width: number
    public height: number
    constructor(sX: number, sY: number, width: number, height: number) {
        this.sX = sX
        this.sY = sY
        this.width = width
        this.height = height
    }
}
export default SpriteImage